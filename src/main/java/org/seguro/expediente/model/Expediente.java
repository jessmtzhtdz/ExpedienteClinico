package org.seguro.expediente.model;

public class Expediente {
	private String idPaciente;
	private String codigoDocumento;
	private String titulo;
	private String tiempoEfectivo;
	private String codigoConfidencialidad;
	private String idMedico;
	private String effectiveTime;
	private String codigoIdioma;
	public String getIdPaciente() {
		return idPaciente;
	}
	public void setIdPaciente(String idPaciente) {
		this.idPaciente = idPaciente;
	}
	public String getCodigoDocumento() {
		return codigoDocumento;
	}
	public void setCodigoDocumento(String codigoDocumento) {
		this.codigoDocumento = codigoDocumento;
	}
	public String getTitulo() {
		return titulo;
	}
	public void setTitulo(String titulo) {
		this.titulo = titulo;
	}
	public String getTiempoEfectivo() {
		return tiempoEfectivo;
	}
	public void setTiempoEfectivo(String tiempoEfectivo) {
		this.tiempoEfectivo = tiempoEfectivo;
	}
	public String getCodigoConfidencialidad() {
		return codigoConfidencialidad;
	}
	public void setCodigoConfidencialidad(String codigoConfidencialidad) {
		this.codigoConfidencialidad = codigoConfidencialidad;
	}
	public String getIdMedico() {
		return idMedico;
	}
	public void setIdMedico(String idMedico) {
		this.idMedico = idMedico;
	}
	public String getEffectiveTime() {
		return effectiveTime;
	}
	public void setEffectiveTime(String effectiveTime) {
		this.effectiveTime = effectiveTime;
	}
	public String getCodigoIdioma() {
		return codigoIdioma;
	}
	public void setCodigoIdioma(String codigoIdioma) {
		this.codigoIdioma = codigoIdioma;
	}
	
	

}
